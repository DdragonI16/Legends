package engine.Stats.stat_bars;

import engine.Player.player;

import java.awt.*;
import java.util.function.Function;

public class bar {

    public static Object bar = new Dimension(50, 10);

    public Function showBar(bar bar) {
        if (player.isPlayerInLevel = true) {
            showBar(bar);
        }
        return (Function) bar;
    }
}